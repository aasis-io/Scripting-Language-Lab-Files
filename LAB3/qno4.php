<?php

// Example array
$numbers = [1, 2, 3, 4, 5];

// Loop through the array
foreach ($numbers as $number) {
    // Check if the number is even
    if ($number % 2 == 0) {
        // Skip even numbers
        continue;
    }
    // Print odd numbers
    echo $number . " ";
}

?>


